package com.learn;

public class Constituency {
	private String constituencyPlace;

	public Constituency(String constituencyPlace) {
		super();
		this.constituencyPlace = constituencyPlace;
	}

	public Constituency() {
		super();
	}

	public String getConstituencyPlace() {
		return constituencyPlace;
	}

	public void setConstituencyPlace(String constituencyPlace) {
		this.constituencyPlace = constituencyPlace;
	}
	
}
